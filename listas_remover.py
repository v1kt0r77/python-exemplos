alunos=['viktor','lucas','carlos','marcos','gustavo','guilherme','andrade','viktor']
print(f"lista de alunos: (alunos)")
# metodo remove()
alunos.remove('viktor')
print(f"listas de alunos: (alunos)")
# metodo pop()
alunos.pop(2)
print(f"lista de alunos: (alunos)")
# função del
del alunos[3]
print(f"lista de alunos: (alunos)")
# metodo clear()
alunos.clear()
print(f"lista de alunos: (alunos)") 